ALL_QUESTIONS = {1: L1, 2: L2, 3: L3, 4: L4, 5: L5}


class Command(BaseCommand):
    help = (
        "Seeds the full story-driven question bank: "
        "'Mars Return Mission вҖ“ Space Station Emergency'. "
        "10 questions Г— 5 subjects Г— 5 levels = 250 questions. Safe to re-run."
    )

    def handle(self, *args, **options):
        created_total = 0
        skipped_total = 0

        for level, subjects in ALL_QUESTIONS.items():
            difficulty = LEVEL_DIFFICULTY[level]
            for subject, questions in subjects.items():
                for q in questions:
                    _, created = Question.objects.get_or_create(
                        level=level,
                        subject=subject,
                        difficulty=difficulty,
                        text=q["text"],
                        defaults={
                            "option_a": q["option_a"],
                            "option_b": q["option_b"],
                            "option_c": q["option_c"],
                            "option_d": q["option_d"],
                            "correct_option": q["correct_option"],
                            "explanation": q["explanation"],
                        },
                    )
                    if created:
                        created_total += 1
                    else:
                        skipped_total += 1

        self.stdout.write(self.style.SUCCESS(
            f"\nвң… Seeding complete!\n"
            f"   Created : {created_total} questions\n"
            f"   Skipped : {skipped_total} already existed\n"
            f"   Total   : {created_total + skipped_total}\n"
        ))
